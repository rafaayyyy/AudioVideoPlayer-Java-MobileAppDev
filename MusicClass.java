package com.example.audiovideoplayer;

public class MusicClass {
    Integer id;
    String name;

    public MusicClass() {
        this.id = null;
        this.name = null;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}